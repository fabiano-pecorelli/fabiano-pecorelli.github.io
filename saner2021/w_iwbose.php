			<p style="text-align: left;"><span class="content">The third International Workshop on Blockchain Oriented Software Engineering is held as a Workshop at <strong>SANER 2021</strong>, and </span>aims at gathering together researchers from the academia and from the industry to focus on the new challenges posed by the new software technology supporting the various blockchains infrastructure. The Workshop tackles once more the challenges of gathering together researchers interested in emerging topics and trends in software engineering. The Workshops goal is to gather together practitioners, researchers and people from industry to discuss the progresses on the research and on the practical usage of blockchain technologies and smart contracts, focusing on the application and definition of software engineering principles and practices specific for such software technology, and for the technologies relying on it.
			</p>
			<p>Such technologies are rapidly evolving and rise more and more interest among scientists, researchers and industry. The focus of BOSE is on the still unsatisfactory application of specific software engineering development best practices to blockchain oriented software and in particular to Smart Contracts.
			</p>
			<p style="text-align: left;">The third Workshop edition introduces a novelty with respect to the previous ones: a session devoted to young researchers and Ph.D students presentations, where they can discuss and expose early research achievements (ERA) or new software products to the audience.
			</p>
			<p style="text-align: left;">Motivations for this workshop are the ever-increasing interest both in the research community and in the industry on Blockchain and smart contracts principles and applications, being the management of cryptocurrencies the most popular topic. Countries like USA and China are heading the run to exploit all blockchain potential. These novelties call for specific tools, paradigms, principles, approaches and research to deal with it and for a specific Blockchain Oriented Software Engineering (BO SE ) [1].
			</p>
			<p style="text-align: left;">This workshop aims at bringing the interest of researchers, practitioners, and people from industry to the current issues and new directions and challenges for blockchain-oriented software engineering and at investigating the need for novel specialized software engineering practices for the Blockchain software sector.
			</p>
			<p>Workshop topics include, not exhaustively , the following:</p>
				<ul>
					<li>Blockchain-Oriented Software Engineering.</li>
					<li>Blockchain software analysis and re-engineering</li>
					<li>Formal specification of Blockchain behavior</li>
					<li>Agile and Lean processes for Blockchain software development</li>
					<li>Tools for Blockchain software distributed development and community management</li>
					<li>Smart Contracts re-engineering</li>
					<li>Security and reliability in Blockchain and Smart Contracts</li>
					<li>Smart Contract Testing (SCT)</li>
					<li>Blockchain Transaction Testing (BTT) to ensure status integrity</li>
					<li>Blockchain Software architecture, design notation and metamodels</li>
					<li>Applications in Economy and Finance</li>
					<li>Internet of Things</li>
					<li>Notarization Supply chain management Web 3.0 e-commerce, e-health, e-democracy social networks.</li>
				</ul>
