                <h4 class="modal-title">srcML a Retrospective: The Trials and Tribulations of Building Real Software in an Academic Environment</h4>
            </div>
            <div class="modal-body">
                <p>
			srcML (<a target="_blank" href="https://nam03.safelinks.protection.outlook.com/?url=http%3A%2F%2Fsrcml.org%2F&data=02%7C01%7Cjmaletic%40kent.edu%7C5958f13bd5784459855b08d7a38222b4%7Ce5a06f4a1ec44d018f73e7dd15f26134%7C1%7C0%7C637157651645137784&sdata=UC4bVFv5w2BKaxQPGXC9if4LPoPY%2F3n1LIJ8E0EhM2s%3D&reserved=0">srcML.org</a>) is an infrastructure consisting of an XML representation for C/C++/C#/Java source code along with efficient parsing technology to convert source code to-and-from the srcML format.  It is widely used by both academic researchers and industry practitioners alike.  The project has been under development for over 15 years, with the first public release in 2005. The focus of the talk is on how the project progressed from a rough-around-the-edges research prototype to a highly scalable and robust software system.  Included is a discussion of the problems encountered while constructing high-quality software in an academic environment.  The need to develop testing platforms for the various builds and releases proves to be a large technical hurdle.  Securing longterm developer expertise is also a struggle.  The intent is to present the experiences of the srcML development team in choosing tools, processes, and platforms to support the wider adoption of the infrastructure.  Additionally, there is an overview of the research that has leveraged srcML that highlights the diversity of projects and topics.  The srcML infrastructure is freely available for download and licensed under GPL.  The website has complete documentation on the format and language support, and downloadable executables for MS Windows, Mac OS, and various GNU Linux distributions.
                </p>
