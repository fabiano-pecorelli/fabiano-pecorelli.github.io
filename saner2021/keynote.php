            </div>
            <div class="modal-body">
                <p>
			Code clones are identical or similar code fragments. Code clones are introduced intentionally or accidentally by copy-and-paste actions. Once created, clones must be maintained as they evolve throughout the lifetime of a software system. Approximately 20% of code can be clones, with clones generally considered as a contributing factor to software defects. Due to the large amounts of clones, the limited resources available and time-to-market pressure, it is often intractable for developers to manage clones. In this talk, I share some of our experience and research findings in the effective management of clones throughout the evolution of large-scale software systems. 
