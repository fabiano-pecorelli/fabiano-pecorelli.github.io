                <h4 class="modal-title">Lessons and Insights from Tech Transfers at Microsoft</h4>
            </div>
            <div class="modal-body">
                <p>
			As a basic industrial research lab, Microsoft Research expects its members to both publish basic research and put it into practice.  Unfortunately, moving from a validated technique or model in a published paper to a state where that same technique is being used by and providing value to software development projects on a regular basis in a consistent and timely fashion is a time consuming, fraught, and difficult task. We have attempted to make this transition, which we call "Tech Transfer", many times in the Software Analysis and Intelligence (SAINT) group at Microsoft Research. Much like research in general, there have been both triumphs and setbacks, but each experience has provided valuable insight and informed our next effort. This talk shares our experiences from successes and failures and provides lessons and guidance that can be used by others trying to transfer their research into practice in both industrial and academic contexts.
                </p>
