                        <p style="text-align: justify;">
			Bug fixing is expensive and the study of it is a hot research topic in software engineering. During the bug fixing process, developers leverage various software artifacts (e.g., bug reports, commits, log files, source files, etc.) and explore multi-source heterogeneous information (Q&A websites, web resources, software communities, etc.) to reproduce the bugs, locate the bugs, identify candidate fixing solutions, apply the fixes and validate the fixes. The rich data can indicate the important information of bug fixing, which can guide developers resolve bugs. For example, a bug report not only shows the details of the reported bug, but also shows the potential method of bug fixing. Therefore, how to analyze and utilize these data is an important step of bug fixing.
                        </p>
			<p style="text-align: justify;">
			The workshop will focus on intelligent bug fixing. Generally, bug fixing process includes bug understanding (i.e., bug reproduction, severity/priority verification, bug summarization, bug classification, bug knowledge extraction), bug localization, bug fixing, and bug validation. By using data mining, information retrieval, machine learning, natural language processing, artificial intelligence technologies, visualization technologies, human-computer interaction technologies and code analysis technologies, a series of new automated algorithms will be proposed to improve the performance of developers bug fixing process. In this workshop, we solicit high-quality contributions with consolidated and thoroughly evaluated application-oriented research results in the area of intelligent bug fixing that are worthy of archival publication in SANER 2021 workshop proceeding. It is intended to (i) provide a summary of research that advances intelligent bug fixing using multiple data analysis and processing techniques, and (ii) serve as a comprehensive collection of some of the current state-of-the-art technologies within this content. The workshop will collaborate with the 27th IEEE International Conference on Software Analysis, Evolution, and Reengineering (SANER 2021).
                        </p>
